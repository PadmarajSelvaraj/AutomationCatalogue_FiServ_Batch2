package JavaTeachings.fundamentals;

public class IncrementAndDecrementDemo1 {
    public static void main(String[] args) {
        int x=10, y=10, a=10, b=10;
        ++x;//x=x+1
        System.out.println("Value of x is  :"+x);//11

        y++;//y=y+1
        System.out.println("Value of y is :"+y);//11

        --a;//a=a-1
        System.out.println("Value of a is :"+a);//9

        b--;//b=b-1
        System.out.println("Value of b is :"+b);//9
    }
}
