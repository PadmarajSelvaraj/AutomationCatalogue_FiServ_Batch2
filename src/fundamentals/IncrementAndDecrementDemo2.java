package fundamentals;

public class IncrementAndDecrementDemo2 {
    public static void main(String[] args) {
        int x=10;
        x++;
        System.out.println("Value of x is :"+x);//11
        ++x;
        System.out.println("Value of x is :"+x);//12
        --x;
        System.out.println("Value of x is :"+x);//11
        x--;
        System.out.println("Value of x is :"+x);//10
    }
}
